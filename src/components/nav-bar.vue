<script>
import NavBarRoutes from './nav-bar-routes.vue'
import { GetSelf } from '@gql/user'

export default {
  components: { NavBarRoutes },
  data() {
    return {
      user: null,
      persistentNavRoutes: [
        {
          name: 'home',
          title: 'Home',
        },
      ],
      loggedInNavRoutes: [
        {
          name: 'user-profile',
          params: () => ({ id: this.user.id }),
          title: () => 'Logged in as ' + this.user.name || this.user.email,
        },
        {
          name: 'logout',
          title: 'Log out',
        },
      ],
      loggedOutNavRoutes: [
        {
          name: 'login',
          title: 'Log in',
        },
      ],
    }
  },
  apollo: {
    user: {
      query: GetSelf,
      update: data => data.self,
    },
  },
}
</script>

<template>
  <ul :class="$style.container">
    <NavBarRoutes :routes="persistentNavRoutes" />
    <NavBarRoutes v-if="user" :routes="loggedInNavRoutes" />
    <NavBarRoutes v-else :routes="loggedOutNavRoutes" />
  </ul>
</template>

